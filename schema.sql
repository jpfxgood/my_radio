DROP DATABASE IF EXISTS MYRADIO;
CREATE DATABASE MYRADIO;
CREATE TABLE MYRADIO.ARTISTS (
ALIAS VARCHAR(40),
ASSOC VARCHAR(1200),
BACKGND VARCHAR(20),
BIRTHNAME VARCHAR(30),
BORN VARCHAR(30),
CAPTION VARCHAR(80),
DIED VARCHAR(20),
GENRE VARCHAR(50),
IMAGE VARCHAR(30),
INSTRUMENT VARCHAR(30),
LABEL VARCHAR(40),
MEMBERS VARCHAR(1500),
NAME VARCHAR(30),
NNAME VARCHAR(30),
ORIGIN VARCHAR(30),
PASTMEMB VARCHAR(2500),
URL VARCHAR(30),
VOICE VARCHAR(20),
YEARS VARCHAR(20),
XML VARCHAR(132),
ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, INDEX (NAME)) CHARACTER SET 'utf8';
CREATE TABLE MYRADIO.ALBUMS (
ARTIST VARCHAR(30),
NARTIST VARCHAR(30),
COVER VARCHAR(40),
GENRE VARCHAR(30),
LABEL VARCHAR(30),
LASTALBUM VARCHAR(40),
NAME VARCHAR(80),
NNAME VARCHAR(80),
NEXTALBUM VARCHAR(40),
PRODUCER VARCHAR(40),
RECORDED VARCHAR(40),
RELEASED VARCHAR(20),
REVIEW VARCHAR(40),
XML VARCHAR(132),
ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, INDEX (NAME), INDEX (ARTIST)) CHARACTER SET 'utf8';
